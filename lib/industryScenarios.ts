export function getIndustryScenario(industryId: string): IndustryScenario | null {
  return INDUSTRY_SCENARIOS[industryId] || null
}

export function getAllIndustryScenarios(): IndustryScenario[] {
  return Object.values(INDUSTRY_SCENARIOS)
}

